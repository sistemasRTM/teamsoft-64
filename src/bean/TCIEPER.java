package bean;

public class TCIEPER {
	private String ejercicio;
	private String periodo;
	private String situacion;
	private String proceso;
	private String usucre;
	private String usumod;
	private String fechacre;
	private String fechamos;
	
	public String getEjercicio() {
		return ejercicio;
	}
	public void setEjercicio(String ejercicio) {
		this.ejercicio = ejercicio;
	}
	public String getPeriodo() {
		return periodo;
	}
	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}
	public String getSituacion() {
		return situacion;
	}
	public void setSituacion(String situacion) {
		this.situacion = situacion;
	}
	public String getProceso() {
		return proceso;
	}
	public void setProceso(String proceso) {
		this.proceso = proceso;
	}
	public String getUsucre() {
		return usucre;
	}
	public void setUsucre(String usucre) {
		this.usucre = usucre;
	}
	public String getUsumod() {
		return usumod;
	}
	public void setUsumod(String usumod) {
		this.usumod = usumod;
	}
	public String getFechacre() {
		return fechacre;
	}
	public void setFechacre(String fechacre) {
		this.fechacre = fechacre;
	}
	public String getFechamos() {
		return fechamos;
	}
	public void setFechamos(String fechamos) {
		this.fechamos = fechamos;
	}
}
